package com.tutorial.pengenalanpart1;

public class namaString {
  public static void main(String[] args) {
    // membuat charakter & string uselly ""/''

    char s = 'S';
    char a = 'A';
    char n = 'N';

    System.out.print(s);
    System.out.print(a);
    System.out.println(n);

    String firstName = "hasan";
    String lastName = "almunawar";
    String fullName = firstName + " " + lastName; //cara menggabungkan 2 string

    System.out.println(firstName);
    System.out.println(lastName);
    System.out.println(fullName);



    // tipe data bolean

    boolean benar = true;
    boolean salah = false;

    System.out.println(benar);
    System.out.println(salah);
  }
  
}
